                mendirikan pondok pesantren yang beliau beri nama Darul Falah Ki Ageng Mbodo atau yang kerap disebut
                Pondok Mbodo.Waktu berjalan,hari berganti,santri yang tinggal di rumah beliau terus bertambah,dengan
                dukungan guru,orang tua,istri,keluarga dan kerabat beliau membangun sebuah bangunan yang berukuran tidak
