                ngaji oleh Mbah Hari(paman dari sang istri) hari demi hari beliau lalui dari beberapa santri yang beliau
                ajar,hingga pada suatu hari beliau bermimpi bertemu dengan seorang ulama’,ulama’ itu berkata “suatu hari
                jika ada yang mengaji lalu ingin menginap di rumahmu maka terimalah”.Dikemudian hari ada beberapa santri
                yang ingin bermalam di rumah beliau mulai dar 3 orang menjadi 9,lalu bertambah menjadi 20,dengan
                berjalannya waktu,bertambahnya hari dan juga bertambahnya santri akhirnya beliau membuat program mengaji
