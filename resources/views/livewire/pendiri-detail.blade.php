                        beliau berjumlah delapan orang, namun tiga saudara lainnya telah meninggal saat masih bayi.
                        <br><br>
                        Di umurnya yang masih lima tahun, Bashir kecil sudah mengerti akan salat dan wirid. Bahkan
                        caranya sudah seperti orang dewasa. Sekitar dua sampai tiga tahun kemudian, ia masuk ke sekolah
                        milik Belanda, Veer Folexs Schooll. Mungkin ada rahasia tersendiri mengapa Kiai Mubin yang
                        notabenenya keluarga santri, menyekolahkan anaknya di situ.
                        <br><br>
                        Semua berjalan dengan lancar hingga beliau berhasil menjadi lulusan siswa terbaik di kelas lima.
                        Sejak lama, sebenarnya para guru ingin menyekolahkan Abdul Bashir di sekolahan favorit, tempat
                        sekolahnya para priyayi. Hingga suatu hari ada salah satu guru yang berkeinginan mengambil
                        Bashir kecil untuk di sekolahkan favorit. Di tengah ketimpangan kedua orang tuanya tentang
                        ekonomi dan kewajiban rohani, Bashir kecil berkata “Aku kok kepingin ngaji, mondok neng Mbareng
                        pak” (Saya kok kepingin ngaji, nyanri di Mbareng pak). Akhirnya mereka sowan ke Mbah Yasin (Mbah
                        Kandar), dan di jawab “Kowe ora usah sekolah guru, mondok wae, besuk kowe dadi guru” (Kamu tidak
                        usah sekolah jadi guru, mondok saja, besok kamu akan jadi guru). Akhirnya beliau pun di antar ke
                        Mbah Arwani Amin Kudus, guru thariqah Kiai Mubin, setelah sebelumnya melaksanakan khitan.
                        <br><br>
                        Sebelum beliau menetap di Pondok Pesantren Mbareng1923 (sekarang PP. Al-Qaumaniyah), sekitar
                        tahun 1940, beliau juga berkesempatan untuk nyantri di PP. Kenepan Langgar Dalem Kudus. Berguru
                        pada KH. Ma’mun Ahmad, khatam al-Qur’an pada KH. Arwani Amin serta masyayikh di sekitar Kudus
                        seperti KH. Irysad dan KH. Khandiq, kakak dari KH. Turaichan Adjhuri Kudus.
                        <br><br>
                        Beliau pun akhirnya menetap di Mbareng dan khidmah di sana. Beliau adalah murid kesayangan Mbah
                        Yasin, bahkan beliau pernah diajak untuk puasa ngebleng (tidak buka) bersama selama tiga hari
                        berturut-turut. Berbagai wirid dan riadlah beliau jalani, hingga Mbah Yasin mengutus beliau
                        untuk menjadi lurah pondok. Beliau juga pernah bergabung dengan GPII (Gerakan Pemuda Islam
                        Indonesia) dan BPRI (Badan Perjuangan Republik Indonesia), sekitar tahun 1944-1945 M.
                        <br><br>
                        Menggantikan Kiai
                        <br><br>
                        Setelah dirasa cukup lama, dan karena para santri semakin banyak, akhirnya Basyir muda di utus
                        untuk mengajar santri-santri junior oleh Mbah Yasin. Sepeninggal Mbah Yasin, bertambahlah jadwal
                        mengajar beliau. Di bantu oleh Kiai Muhammad (putra Mbah Yasin) dan Kiai Hanafi (menantu Mbah
                        Yasin), mereka meneruskan perjuangan Mbah Yasin.
                        <br><br>
                        Menikah dan Membangun Pondok
                        <br><br>
                        Beliau akhirnya menikah dengan putri H. Abdul Ghoni, Sholehah, yang umurnya masih sangat muda.
                        Sejak saat itu beliau tidak menginap di pondok, tapi pulang ke rumah. Hingga dua tahun kemudian
                        beliau membuat rumah sendiri di Jekulo, Kauman.
                        <br><br>
                        Di penghujung tahun 60-an ada seseorang yang namanya sama dengan beliau mewakafkan tanah berikut
                        rumah sokowulu miliknya untuk dijadikanpondok. Letaknya persis tepat di sebelah utara masjid
                        Kauman. Pondok ini kemudian dinamai dengan Darul Falah setelah sebelumnya hanya berinisial
                        dengan sebutan “Pondok D”. Setelah sekian lama berjalan, sekitar tahun 1992 M., akhirnya beliau
                        membangun asrama putri. Santri putri di tahun pertama mencapai 5 orang, tahun kedua 9 orang,
                        hingga sekarang pondok ini telah menampung lebih dari 600 santri perempuan.
                        Mbah Basyir baru di karuniai anak setelah dua tahun menikah. Akhirnya, saat beliau dan Nyai
                        Sholehah menetap di Hadiwarno, Nyai Sholehah melahirkan anak pertamanya, Dewi Umniyah, disusul
                        ketujuh adik-adiknya; Inaroh, Amti’ah, Ahmad Badawi, Arikhah, Muhammad Jazuli, Muhammad Asyiq
                        (almarhum), Nur Zakiyah Mabruroh, dan Muhammad Alamul Yaqin.
                        <br><br>
                        Teladan Dari Beliau
                        <br><br>
                        Beliau adalah kiai yang sangat patuh dan taat pada kiai, senang tirakat dan riadlah. Ziarah
                        adalah hobinya sejak masih muda. Sangat ‘alim dan ahli dzikir, begitulah beliau dikenal. Dengan
                        senyuman yang khas dan keramahan, beliau selalu menerima santri dan tamu yang mengantre, ingin
                        mendapatkan ijazah.
                        <br><br>
                        Dalam pendidikan, beliau mengajarkan putra-putranya lebih keras dibanding para santri.
                        Keseharian beliau adalah mengajar santri, menemui tamu, bercengkrama dengan keluarga dan
                        muthola’ah kitab. Selepas Subuh, beliau selalu menyempatkan dirinya untuk sowan ke pesarehan
                        Mbah Yasin, guru yang telah memberinyaijazah Dala’il al-Khairat.
