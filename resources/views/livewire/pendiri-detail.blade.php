                        Biografi
                        <br><br>
                        Abdul Bashir adalah nama kecilnya. Konon, nama itu di beri oleh seorang sayyid ketika ibu beliau
                        sedang hamil tua. Ayah beliau bernama Kiai Muhammad Mubin atauMbah Kasno, seorang penjahit
